#Return the number (count) of vowels in the given string. We will consider a, e, i, o, u as vowels for this Kata (but not y).

def get_count(input_str)
    letters = input_str.downcase.split("")
    vowels = ["a", "e", "i", "o", "u"]
    count = 0
    letters.each do |ind_lett|
        count += 1 if vowels.include?(ind_lett)
    end
    puts count
end

